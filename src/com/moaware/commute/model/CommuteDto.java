package com.moaware.commute.model;

public class CommuteDto {

	private String emp_num;
	private String commute_date;
	private String check_in;
	private String check_out;
	private String commute_division;

	public String getEmp_num() {
		return emp_num;
	}

	public void setEmp_num(String emp_num) {
		this.emp_num = emp_num;
	}

	public String getCommute_date() {
		return commute_date;
	}

	public void setCommute_date(String commute_date) {
		this.commute_date = commute_date;
	}

	public String getCheck_in() {
		return check_in;
	}

	public void setCheck_in(String check_in) {
		this.check_in = check_in;
	}

	public String getCheck_out() {
		return check_out;
	}

	public void setCheck_out(String check_out) {
		this.check_out = check_out;
	}

	public String getCommute_division() {
		return commute_division;
	}

	public void setCommute_division(String commute_division) {
		this.commute_division = commute_division;
	}

}
